// Утилиты для работы со шрифтами в PDF
export const registerRussianFont = (doc) => {
  // В этой версии используем стандартные шрифты
  // Для поддержки кириллицы можно добавить кастомные шрифты
  return 'helvetica';
};

// Функция для безопасного отображения русского текста
export const safeRussianText = (text) => {
  if (typeof text !== 'string') return String(text);
  
  // Заменяем проблемные символы, но сохраняем кириллицу
  return text
    .replace(/[^\u0400-\u04FF\w\s.,%()+-]/g, '') // Сохраняем кириллицу и базовые символы
    .replace(/"/g, "'") // Заменяем кавычки
    .replace(/\n/g, ' '); // Заменяем переносы строк
};
